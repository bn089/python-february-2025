numbers={1,2,3,4,5,6,2,3,4}
print(numbers)  # Output: {1, 2, 3, 4, 5, 6} (duplicates removed)

numbers.add(7)  # Adding a new item to the set
print(numbers)  



# Set is a collection of unique items
# It does not allow duplicate values
numbers.remove(2)  # Removing an item from the set
print(numbers)  # Output: {1, 3, 4, 5,
# 6, 7} (2 is removed)
